<template>
    <div>
    	<head-top head-title="Planned" goBack="true"></head-top>
    	<nav class="msite_nav">
    		<div class="swiper-container" v-if="foodTypes.length">
		        <div class="swiper-wrapper">
		            <div class="swiper-slide food_types_container" v-for="(item, index) in foodTypes" :key="index">
	            		<router-link :to="{ query: {geohash, title: foodItem.title, restaurant_category_id: getCategoryId(foodItem.link)}}" v-for="foodItem in item" :key="foodItem.id" class="link_to_food">
	            			<figure>
	            				<img :src="imgBaseUrl + foodItem.image_url" class="search_submit" @click.prevent="searchTarget(foodItem.title)">
	            				<figcaption>{{foodItem.title}}</figcaption>
	            			</figure>
	            		</router-link>
		            </div>
		        </div>
		        <div class="swiper-pagination"></div>
		    </div>

		    <img src="../../images/fl.svg" class="fl_back animation_opactiy" v-else>
		    <div>
		    	<section v-if="restaurantList.length">
	            <h4 class="title_restaurant">events</h4>
	            <ul class="list_container">
	                <router-link :to="{ query:{id:item.id}}" tag="li" v-for="item in restaurantList" :key="item.id" class="list_li">
	                    <section class="item_left">
	                        <img :src="item.pictureUri" class="restaurant_img">
	                    </section>
	                    <section class="item_right">
	                        <div class="item_right_text">
	                            <p>
	                                <span>{{item.name}} </span>
