<script>
import {mapMutations} from 'vuex'
// import {imgBaseUrl} from 'src/config/env'
import headTop from 'src/components/header/head'
import footGuide from 'src/components/footer/footGuide'
import shopList from 'src/components/common/shoplist'
import {msiteAddress, msiteFoodTypes, cityGuess} from 'src/service/getData'
import 'src/plugins/swiper.min.js'
import 'src/style/swiper.min.css'
import json from 'src/json/description.json'
import {AXIOS} from './http-common'

export default {
	data(){
        return {
        	geohash: '', 
            msiteTitle: 'Please input your address...', 
            foodTypes: [], 
            restaurantList: [],
            hasGetData: false, 
            imgBaseUrl: 'https://fuss10.elemecdn.com', 
        }
    },
    async beforeMount(){
		if (!this.$route.query.geohash) {
			const address = await cityGuess();
			this.geohash = address.latitude + ',' + address.longitude;
		}else{
			this.geohash = this.$route.query.geohash
		}
		this.SAVE_GEOHASH(this.geohash);
    	let res = await msiteAddress(this.geohash);
    	this.msiteTitle = res.name;
    	this.RECORD_ADDRESS(res);

    	this.hasGetData = true;
    },
    mounted(){
       	msiteFoodTypes(this.geohash).then(res => {
       		let resLength = res.length;
       		let resArr = json; 

       		let foodArr = [];
    		for (let i = 0, j = 0; i < resLength; i += 8, j++) {
    			foodArr[j] = resArr.splice(0, 8);
    		}
    		this.foodTypes = foodArr;
        }).then(() => {
        	new Swiper('.swiper-container', {
		        pagination: '.swiper-pagination',
		        loop: true
		    });
        })
    },
    components: {
    	headTop,
    	shopList,
    	footGuide,
    },
    computed: {

    },
    methods: {
    	...mapMutations([
    		'RECORD_ADDRESS', 'SAVE_GEOHASH'
    	]),
    	getCategoryId(url){
    		let urlData = decodeURIComponent(url.split('=')[1].replace('&target_name',''));
    		if (/restaurant_category_id/gi.test(urlData)) {
    			return JSON.parse(urlData).restaurant_category_id.id
    		}else{
    			return ''
    		}
    	},
    	async searchTarget(historyValue){	
    		console.log(historyValue);
    		if (historyValue === "Food"){
    			this.restaurantList = [];
    			AXIOS.get(`/favorite?type=1&userId=SB`)
	            .then(response => {
	              // JSON responses are automatically parsed.
	              this.restaurantList = response.data
	            })
	            .catch(e => {
	              this.errors.push(e)
	            })
    		}
    		else if (historyValue === "Play"){
    			this.restaurantList = [];
    			AXIOS.get(`/favorite?type=0&userId=SB`)
	            .then(response => {
	              // JSON responses are automatically parsed.
	              this.restaurantList = response.data
	            })
	            .catch(e => {
	              this.errors.push(e)
	            })
    		}
    		else if (historyValue === "Market"){
    			this.restaurantList = [];
    			AXIOS.get(`/favorite?type=2&userId=SB`)
	            .then(response => {
	              // JSON responses are automatically parsed.
	              this.restaurantList = response.data
	            })
	            .catch(e => {
	              this.errors.push(e)
	            })
    		}
    		else{
    			this.restaurantList = [];
    		}

    	}
    	
    },
    watch: {

    }
}

</script>

